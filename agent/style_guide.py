STYLE_GUIDE = """
VOICE AND TONE:
- First person throughout. Write as Olayinka, not about Olayinka.
- Conversational but intelligent. Write like a sharp engineer
  talking to a peer, not a professor lecturing students.
- Honest over impressive. Say what's true, including what you
  don't know yet.
- Values come through naturally — African tech context, building
  things that matter, security-minded thinking — never stated
  directly, always shown through examples and choices.
- Interesting over formal. If a sentence is boring, cut it or
  rewrite it.

STRUCTURE:
- Opening hook: one strong statement or honest admission.
  No "In this post I will..." ever.
- 3-5 sections with colon-separated headings e.g.
  "Why this matters: " not "## Why This Matters"
- End with something forward-looking or an invitation to respond.
- Length: 600-900 words. Tight. No padding.

LANGUAGE RULES:
- No em dashes. Use commas, colons, or a new sentence instead.
- No "In conclusion", "To summarize", "It's worth noting".
- No bullet points in the post body. Write in prose.
- Short paragraphs. Max 3-4 sentences each.
- Contractions are fine. "I'm", "it's", "you'll" — natural speech.
- Nigeria and Africa get mentioned where genuinely relevant,
  never forced.

WHAT TO AVOID:
- Hype language: "revolutionary", "game-changing", "cutting-edge"
- Vague openings: "AI is changing everything..."
- Passive voice where active works
- Explaining what the post is about instead of just writing it
"""
